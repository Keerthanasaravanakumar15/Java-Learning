public class datatype {
  public static void main(String[] args) {
      long num = 21474836487L;
      System.out.println(num);
      float a = 1.2345678f;
      System.out.println(a);
      double b = 1.23456789;
      System.out.println(b);
      char c = 'K';
      System.out.println(c);
      boolean d = true;
      System.out.println(d);
  }
  
}
